x = 10
y = 5

# Condizione semplice
if (x > y):
    print("x è maggiore di y")


if (x < y):
    print("x è minore di y")
# Condizione con else
else:
    print("x non è minore di y")

# Condizione con elif
if (x == y):
    print("x è uguale a y")
elif (x > y):
    print("x è maggiore di y")
else:
    print("x è minore di y")